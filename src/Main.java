public class Main {
    public static void main(String[] args) {
        int numToExamine = 4;
        int modulo = numToExamine % 2;
        System.out.println( " The number " + numToExamine + " is even because the remainder of " + numToExamine + " divided by 2 is " + modulo);
        numToExamine = 3;
        modulo = numToExamine % 2;
        System.out.println( " The number " + numToExamine + " is odd because the remainder of " + numToExamine + " divided by 2 is " + modulo);
    }
}